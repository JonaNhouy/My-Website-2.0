    <h2>Optimisation logiciel caisse grande surface</h2>
    <p>A mes expériences, on pourrait réduire le nombre de tâches à effectuer en caisse afin de faciliter le passage des articles</p>
    <ul>
        <li>Carte BIO présenté par le client
            <br>-> Une touche "BIO" à activer afin que les articles concernés passent automatiquement en réduction.</li>
        <br><li>Réduction sur les articles proche de la date de péremption
            <br>-> Automatiquer via un QR code unique par groupe d'articles périmant à la même date afin d'automatiquer la réduction.</li>
        <br><li>Classer les articles sur le ticket de caisse par catégorie lors de l'impression (Fonctionnalité vue à Carrefour en Belgique)</li>
        <br><li>Enregistrer les bons de réductions du magasin sur le compte du client (& l'imprimer aussi pour ceux qui ne souhaite pas avoir
            que du dématérialisé) pour éviter le maximum d'oubli.
        </li>
        <br><li>Lors du passage des articles, afficher : Prix de base, Remise (+ Nom de la promotion/solde), Prix final du produit</li>
        <br><li>Affichage Total composé de : Total alimentaire (Cheque/Carte déjeuner) & le reste + Points fidélités</li>
        <br><li>Affichage constant du Total</li>
    </ul>
